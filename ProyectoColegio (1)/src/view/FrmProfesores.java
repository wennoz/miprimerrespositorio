    public void listar() {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("identificacion");
        modelo.addColumn("Nombre");
        modelo.addColumn("Apellido");
        modelo.addColumn("Edad");
        TblProfesores.setModel(modelo);
        for (ClasePersona persona : ListaProfesor) {
            String edad = Integer.toString(persona.getEdad());
            String[] Fila = new String[4];
            Fila[0] = persona.getIdentificacion();
            Fila[1] = persona.getNombre();
            Fila[2] = persona.getApellido();
            Fila[3] = edad;
            modelo.addRow(Fila);
        }

    }

    public boolean validaciones() {
        boolean validaciones = true;
        if (TxtIdentificacion.getText().length() < 6) {
            JOptionPane.showMessageDialog(null, "la identificaci칩n es minima de 6 digitos");
            validaciones = false;
        }
        if (Integer.parseInt(TxtEdad.getText()) > 130) {
            JOptionPane.showMessageDialog(null, "la edad maxima es 130 a침os");
            validaciones = false;

        }
        return validaciones;
    }

